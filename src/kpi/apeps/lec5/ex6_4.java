package kpi.apeps.lec5;

public class ex6_4 {

    public static void main(String[] args) {

        //-
        int i = 1;
        while (i<10){
            System.out.println(i);
            i++;
        }
        //+
        int j = 0;
        while (++j<10){
            System.out.println(j);
        }
    }
}